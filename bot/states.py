# ===================================================
# states.py — Конечный автомат состояний (FSM)
# ===================================================
# FSM (Finite State Machine) — механизм aiogram,
# который позволяет "помнить" на каком шаге диалога
# находится каждый пользователь.
#
# Каждый пользователь имеет своё НЕЗАВИСИМОЕ состояние.
# Когда пользователь A выбирает услугу, это никак
# не влияет на состояние пользователя B.
#
# Переходы состояний (клиент):
#   START → choosing_service → choosing_date →
#   choosing_time → waiting_phone → DONE
#
# Переходы состояний (админ):
#   ADMIN_MENU → различные подменю
# ===================================================

from aiogram.fsm.state import State, StatesGroup


# ===================================================
# СОСТОЯНИЯ КЛИЕНТСКОГО ФЛОУ (ЗАПИСЬ)
# ===================================================

class BookingStates(StatesGroup):
    """
    Группа состояний для процесса записи клиента.
    StatesGroup автоматически именует состояния:
    "BookingStates:choosing_service" и т.д.
    """

    # Пользователь выбирает услугу из списка кнопок
    choosing_service = State()

    # Пользователь выбирает дату в инлайн-календаре
    choosing_date = State()

    # Пользователь выбирает время из доступных слотов
    choosing_time = State()

    # Бот ждёт номер телефона (через кнопку или вручную)
    waiting_phone = State()


# ===================================================
# СОСТОЯНИЯ ADMIN-ПАНЕЛИ — УПРАВЛЕНИЕ УСЛУГАМИ
# ===================================================

class AdminServiceStates(StatesGroup):
    """
    Состояния для добавления и редактирования услуги.
    Добавление: название → длительность → цена (3 шага).
    Редактирование: те же шаги, каждый можно пропустить.
    """

    # --- Добавление новой услуги ---

    # Ожидание названия услуги (текстовый ввод)
    waiting_name = State()

    # Ожидание выбора длительности (кнопки с вариантами)
    waiting_duration = State()

    # Ожидание ввода цены (текстовый ввод)
    waiting_price = State()

    # --- Редактирование существующей услуги ---

    # Шаг 1/3: новое название (или Пропустить)
    waiting_edit_name = State()

    # Шаг 2/3: новая длительность (или Пропустить)
    waiting_edit_duration = State()

    # Шаг 3/3: новая цена (или Пропустить / Сохранить)
    waiting_edit_price = State()


